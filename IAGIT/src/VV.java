

public class VV {
	
	private Vertice v;
	private int valor;
	
	public VV(Vertice v, int valor) {
		super();
		this.v = v;
		this.valor = valor;
	}
	
	public Vertice getV() {
		return v;
	}

	public void setV(Vertice v) {
		this.v = v;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}	
	

}
