

public class VV {
	
	private Vertice v;
	private double valor;
	
	public VV(Vertice v, double valor) {
		super();
		this.v = v;
		this.valor = valor;
	}
	
	public Vertice getV() {
		return v;
	}

	public void setV(Vertice v) {
		this.v = v;
	}

	public double getValor() {
		return valor;
	}

	public void setValor(double dTotal) {
		this.valor = dTotal;
	}	
	

}
